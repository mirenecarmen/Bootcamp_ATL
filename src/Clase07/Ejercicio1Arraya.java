package Clase07;

public class Ejercicio1Arraya {
    public static void main(String[] args){
        int[] intArray;
        intArray = new int[5];
        intArray[0] = 100;
        intArray[1] = 130;
        intArray[2] = 150;
        intArray[3] = 160;
        intArray[4] = 170;

        System.out.println("Elemento en el indice 0: "
                + intArray[0]);
        System.out.println("Elemento en el indice 1: "
                + intArray[1]);
        System.out.println("Elemento en el indice 2: "
                + intArray[2]);
        System.out.println("Elemento en el indice 3: "
                + intArray[3]);
        System.out.println("Elemento en el indice 4: "
                + intArray[4]);

    }
}
